export function deductInventoryForShipment(products, inventoryItems) {
  return inventoryItems.map(item => {
    const ordered = products.find(p => p.sku === item.sku);
    if (!ordered) return item;
    const qty = ordered.requestedQty || 0;
    return {
      ...item,
      warehouseStock: Math.max(0, (item.warehouseStock || 0) - qty),
      withCarrierStock: (item.withCarrierStock || 0) + qty,
    };
  });
}
export function returnInventoryFromCarrier(products, inventoryItems) {
  return inventoryItems.map(item => {
    const returned = products.find(p => p.sku === item.sku);
    if (!returned) return item;
    const qty = returned.requestedQty || 0;
    return {
      ...item,
      warehouseStock: (item.warehouseStock || 0) + qty,
      withCarrierStock: Math.max(0, (item.withCarrierStock || 0) - qty),
    };
  });
}
